--DATE EXPLORATION
---------
---------


-- Find the date of the first and last order
--How many years of sales are available in the data?
select
    MIN(order_date) AS first_order_date,
    MAX(order_date) AS last_order_date,
    EXTRACT (YEAR FROM MAX(order_date)) -  EXTRACT(YEAR FROM MIN(order_date)) AS order_range_years
FROM gold.fact_sales;

-- Find the youngest and oldest customers
SELECT
    MIN(birthdate) AS oldest_customer_birthdate,
    MAX(birthdate) AS youngest_customer_birthdate,

    EXTRACT(YEAR FROM AGE(CURRENT_DATE, MIN(birthdate))) 
        AS oldest_age,

    EXTRACT(YEAR FROM AGE(CURRENT_DATE, MAX(birthdate))) 
        AS youngest_age,

    EXTRACT(YEAR FROM MAX(birthdate))
    - EXTRACT(YEAR FROM MIN(birthdate))
        AS age_range_years

FROM gold.dim_customers;


