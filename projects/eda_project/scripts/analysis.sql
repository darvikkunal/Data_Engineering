-- Find total customers by countries

select country, count(DIStiNCT customer_key) as total_customers 
from gold.dim_customers
group by 1
order by 2 desc;

-- Find total customers by gender

select gender, count(distinct customer_key) as total_customers
from gold.dim_customers
group by 1
order by 2 desc;

-- Find total products by category

select category, count(DISTINCT product_key) as total_products
from gold.dim_products
group by 1
order by 2 desc;

-- What is the average costs in each category?

select category , avg(cost) as average_cost
from gold.dim_products
group by 1
order by 2 desc;

-- What is the total revenue generated for each category?

select p.category, sum(f.sales_amount) as total_revenue
from gold.fact_sales f
LEFT join gold.dim_products p 
ON f.product_key = p.product_key
group by 1
order by 2 desc;


-- Find total revenue is generated by each customer

select 
    c.customer_id, c.first_name, c.last_name,
   sum(f.sales_amount) as total_revenue
from gold.fact_sales f
LEFT join gold.dim_customers c
ON f.customer_key = c.customer_key
group by 1,2,3
order by 4 desc;

-- What is the distribution of sold items across countries?
select 
    c.country,
   sum(f.quantity) as total_distribution
from gold.fact_sales f
LEFT join gold.dim_customers c
ON f.customer_key = c.customer_key
group by 1
order by 2 desc;

-- which 5 products generate the highest revenue?
select p.product_name,p.subcategory, sum(f.sales_amount) as total_revenue
from gold.fact_sales f
LEFT join gold.dim_products p 
ON f.product_key = p.product_key
group by 1,2
order by 2 desc
limit 5;

-- RANK func
select * from (
    select 
        p.product_name,sum(f.sales_amount) as total_revenue,
        row_number() OVER(order by sum(f.sales_amount) DESC) as rank_products
    from gold.fact_sales f
    LEFT join gold.dim_products p 
    ON f.product_key = p.product_key
    group by 1
) t
where rank_products <= 5;



-- what are the 5 worst performing products in terms of sales?
select p.product_name, p.subcategory,sum(f.sales_amount) as total_revenue
from gold.fact_sales f
LEFT join gold.dim_products p 
ON f.product_key = p.product_key
group by 1,2
order by 2
limit 5;


--- Find the top-10 customers who have generated the highest revenue and 3 customers with the fewest orders placed
select 
    c.customer_id, c.first_name, c.last_name,
   sum(f.sales_amount) as total_revenue
from gold.fact_sales f
LEFT join gold.dim_customers c
ON f.customer_key = c.customer_key
group by 1,2,3
order by 4 desc
limit 10;

-- lowest 3 customers with fewest orders placed

select 
    c.customer_id, c.first_name, c.last_name,
   count(DISTINCT order_number) as total_orders
from gold.fact_sales f
LEFT join gold.dim_customers c
ON f.customer_key = c.customer_key
group by 1,2,3
order by 4 asc
limit 3;
